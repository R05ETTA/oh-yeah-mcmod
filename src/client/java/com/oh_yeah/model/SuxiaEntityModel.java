    @Override
    public void setAngles(T entity, float limbAngle, float limbDistance, float animationProgress, float headYaw, float headPitch) {
        this.modelRoot.resetTransform();
        this.body.resetTransform();
        this.tail.resetTransform();
        float swimTailYaw = sampleSwimTailYaw(animationProgress);
        float swimTailYawDeg = MathHelper.wrapDegrees(swimTailYaw * MathHelper.DEGREES_PER_RADIAN);
        this.tail.yaw = swimTailYawDeg * MathHelper.RADIANS_PER_DEGREE;
    }

    private static float sampleSwimTailYaw(float animationProgress) {
        float ticks = animationProgress % SWIM_PERIOD_TICKS;
        if (ticks < 0.0F) {
            ticks += SWIM_PERIOD_TICKS;
        }
        float t = ticks / 20.0F;
        for (int i = 0; i < SWIM_KEYFRAME_TIMES.length - 1; i++) {
            float t0 = SWIM_KEYFRAME_TIMES[i];
            float t1 = SWIM_KEYFRAME_TIMES[i + 1];
            if (t <= t1) {
                float alpha = (t - t0) / (t1 - t0);
                float y0 = SWIM_KEYFRAME_YAWS_DEG[i];
                float y1 = SWIM_KEYFRAME_YAWS_DEG[i + 1];
                return MathHelper.RADIANS_PER_DEGREE * MathHelper.lerp(alpha, y0, y1);
            }
        }
        float last = SWIM_KEYFRAME_YAWS_DEG[SWIM_KEYFRAME_YAWS_DEG.length - 1];
        return MathHelper.RADIANS_PER_DEGREE * last;
    }

